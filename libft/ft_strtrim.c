#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*trimmed;
	size_t	start;
	size_t	end;
	int		len;

	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, end[s1 - 1]))
		end--;
	len = end - start;
	trimmed = ft_substr(s1, start, len);
	return (trimmed);
}
